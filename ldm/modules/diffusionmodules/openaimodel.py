class QKVAttention(nn.Module):
    """
    A module which performs QKV attention and splits in a different order.
    """

    def __init__(self, n_heads):
        super().__init__()
        self.n_heads = n_heads

    def forward(self, qkv):
        """
        Apply QKV attention.
        :param qkv: an [N x (3 * H * C) x T] tensor of Qs, Ks, and Vs.
        :return: an [N x (H * C) x T] tensor after attention.
        """
        bs, width, length = qkv.shape
        assert width % (3 * self.n_heads) == 0
        ch = width // (3 * self.n_heads)
        q, k, v = qkv.chunk(3, dim=1)
        scale = 1 / math.sqrt(math.sqrt(ch))
        weight = th.einsum(
            "bct,bcs->bts",
            (q * scale).view(bs * self.n_heads, ch, length),
            (k * scale).view(bs * self.n_heads, ch, length),
        )  # More stable with f16 than dividing afterwards
        weight = th.softmax(weight.float(), dim=-1).type(weight.dtype)
        a = th.einsum("bts,bcs->bct", weight, v.reshape(bs * self.n_heads, ch, length))
        return a.reshape(bs, -1, length)

    @staticmethod
    def count_flops(model, _x, y):
        return count_flops_attn(model, _x, y)
    

class MutualQKVAttention(nn.Module):
    def __init__(self, n_heads):
        super().__init__()
        self.n_heads = n_heads
    
    def forward(self, qkv1, qkv2):
        assert qkv1.shape == qkv2.shape
        bs, width, length = qkv1.shape
        assert width % (3 * self.n_heads) == 0
        ch = width // (3 * self.n_heads)
        q1, k1, v1 = qkv1.chunk(3, dim=1)
        q2, k2, v2 = qkv2.chunk(3, dim=1)
        scale = 1 / math.sqrt(math.sqrt(ch))
        k = th.cat([k1, k2], dim=-1)
        v = th.cat([v1, v2], dim=-1)
        weight1 = th.einsum(
            "bct,bcs->bts",
            (q1 * scale).view(bs * self.n_heads, ch, length),
            (k * scale).view(bs * self.n_heads, ch, length),
        )
        weight1 = th.softmax(weight1.float(), dim=-1).type(weight1.dtype)
        a1 = th.einsum("bts,bcs->bct", weight1, v.reshape(bs * self.n_heads, ch, length))

        weight2 = th.einsum(
            "bct,bcs->bts",
            (q2 * scale).view(bs * self.n_heads, ch, length),
            (k2 * scale).view(bs * self.n_heads, ch, length),
        )
        weight2 = th.softmax(weight2.float(), dim=-1).type(weight2.dtype)
        a2 = th.einsum("bts,bcs->bct", weight2, v2.reshape(bs * self.n_heads, ch, length))

        return a1.reshape(bs, -1, length), a2.reshape(bs, -1, length)


